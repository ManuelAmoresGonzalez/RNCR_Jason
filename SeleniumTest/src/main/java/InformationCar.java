public class InformationCar {

    public String marca;
    public String style;
    public String valorHacienda;
    public String propietario;

    public InformationCar(String marca, String style, String valorHacienda, String propietario){

        this.marca = marca;
        this.style = style;
        this.valorHacienda = valorHacienda;
        this.propietario = propietario;
    }
}
